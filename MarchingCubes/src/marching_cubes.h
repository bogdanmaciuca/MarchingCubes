class MarchingCubes {
public:
	std::vector<glm::vec3> vertices;
	Cell* cells;
	void AllocCellMem() {
		cells = (Cell*)malloc(sizeof(Cell) * (CHUNK_WIDTH+1) * (CHUNK_HEIGHT+1) * (CHUNK_WIDTH+1));
	}
	void GenerateCells(int x0, int y0) {
		for (int x = 0; x < CHUNK_WIDTH + 1; x++) {
			for (int z = 0; z < CHUNK_WIDTH + 1; z++) {
				for (int y = 0; y < CHUNK_HEIGHT + 1; y++) {
					//float height = 10*noise3(x / 12.0f, y / 12.0f, z/ 12.0f);
					float height = 20 + 4 * noise2(x / 11.0f, z / 11.0f) - y;
					height = glm::clamp(height, -1.0f, 1.0f);
					BYTE value = (BYTE)((float)(height + 1) * 127.0);
					cells[x][y][z].terrain = value;
				}
			}
		}
	}
	void CreateMeshData(int x0, int z0) {
		ClearMeshData();
		// Loop through each "cube" in our terrain.
		for (int x = 0; x < CHUNK_WIDTH; x++) {
			for (int y = 0; y < CHUNK_HEIGHT; y++) {
				for (int z = 0; z < CHUNK_WIDTH; z++) {
					BYTE cube[8];
					for (int i = 0; i < 8; i++) {
						glm::vec3 corner = glm::vec3(x0 + x, y, z0 + z) + CornerTable[i];
						Cell c = grid[(int)corner.x][(int)corner.y][(int)corner.z];
						cube[i] = c.terrain;
					}
					// Pass the value into our MarchCube function.
					MarchCube(glm::vec3(x0 + x, y, z0 + z), cube);
				}
			}
		}
	}
	void MarchCube(glm::vec3 position, BYTE* cube) {
		// Get the configuration index of this cube.
		int configIndex = GetCubeConfiguration(cube);

		// If the configuration of this cube is 0 or 255 (completely inside the terrain or completely outside of it) we don't need to do anything.
		if (configIndex == 0 || configIndex == 255)
			return;

		// Loop through the triangles. There are never more than 5 triangles to a cube and only three vertices to a triangle.
		int edgeIndex = 0;
		for (int i = 0; i < 5; i++) {
			for (int p = 0; p < 3; p++) {
				// Get the current indice. We increment triangleIndex through each loop.
				int indice = TriangleTable[configIndex][edgeIndex];

				// If the current edgeIndex is -1, there are no more indices and we can exit the function.
				if (indice == -1) {
					return;
				}

				// Get the vertices for the start and end of this edge.
				glm::vec3 vert1 = position + EdgeTable[indice][0];
				glm::vec3 vert2 = position + EdgeTable[indice][1];

				//// Get the terrain values at either end of our current edge from the cube array created above.
				BYTE vert1Sample = cube[EdgeIndexes[indice][0]];
				BYTE vert2Sample = cube[EdgeIndexes[indice][1]];
				
				// Calculate the difference between the terrain values.
				float difference = vert2Sample - vert1Sample;
				
				// If the difference is 0, then the terrain passes through the middle.
				if (difference == 0)
					difference = TERRAIN_SURFACE;
				else
					difference = (TERRAIN_SURFACE - vert1Sample) / difference;
				
				// Calculate the point along the edge that passes through.
				glm::vec3 vertPosition = vert1 + ((vert2 - vert1) * difference);
				//glm::vec3 vertPosition = (vert1 + vert2) / 2.0f;
				
				// Add to our vertices and triangles list and incremement the edgeIndex.
				vertices.push_back(vertPosition);
				edgeIndex++;
			}
			glm::vec3 A = vertices[vertices.size() - 1];
			glm::vec3 B = vertices[vertices.size() - 2];
			glm::vec3 C = vertices[vertices.size() - 3];
			glm::vec3 AB = A-B, AC = A - C;
			glm::vec3 normal = glm::cross(AB, AC);
			vertices.insert(vertices.begin() + vertices.size() - 2, normal);
			vertices.insert(vertices.begin() + vertices.size() - 1, normal);
			vertices.insert(vertices.begin() + vertices.size(), normal);
		}
	}

	int GetCubeConfiguration(BYTE *cube) {
		BYTE terrain_surface = TERRAIN_SURFACE;
		// Starting with a configuration of zero, loop through each point in the cube and check if it is below the terrain surface.
		int configurationIndex = 0;
		for (int i = 0; i < 8; i++) {
			// If it is, use bit-magic to the set the corresponding bit to 1. So if only the 3rd point in the cube was below
			// the surface, the bit would look like 00100000, which represents the integer value 32.
			if (cube[i] > terrain_surface)
				configurationIndex |= 1 << i;
		}
		return configurationIndex;
	}
	void ClearMeshData() {
		vertices.clear();
	}
};
